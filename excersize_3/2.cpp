#include <iostream>

using namespace std;

int main() {

  int a, b;

  cout << "Enter two integer values with space in between: ";
  cin >> a >> b;

  cout << "a + b = " << a + b << endl;
  cout << "a - b = " << a - b << endl;
  cout << "a * b = " << a * b << endl;
  cout << "a / b = " << a / b << endl;
  cout << "a % b = " << a % b << endl;

  return 0;
}
